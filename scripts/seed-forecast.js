// Демо-прогноз дня для приёмки этапа 3.
// В проде запись обновляется через Telegram-бота (этап 5); этот сид нужен,
// чтобы блок «Прогноз дня» было что показать локально. Идемпотентен:
// если прогноз на сегодня уже есть — ничего не делает.
import { db } from '../src/lib/db.js';

const today = new Date().toISOString().slice(0, 10); // YYYY-MM-DD

const existing = db.prepare('SELECT id FROM forecast WHERE date = ?').get(today);
if (existing) {
  console.log(`Прогноз на ${today} уже есть (id ${existing.id}) — пропускаю.`);
  process.exit(0);
}

const title = 'Прогноз от Геннадия Серафимовича';

const summary = `28-й Солнечный день, 1/2 Лунные дни

Сегодня день не для подвигов и не для новых начинаний. Закрывайте хвосты, доделывайте начатое, наводите порядок в доме, голове и мастерской.

Вода дня: настойки на воде — к исцелению.
Цвет дня: чёрный, тёмно-синий, коричневый.
Еда дня: раздельное питание. Простой хлеб — во благо.
Совет дня: не начинай новое. Доделай старое.`;

const body = `28-й Солнечный день, 1/2 Лунные дни

Сегодня день не для подвигов и не для новых начинаний. Закрывайте хвосты, доделывайте начатое, наводите порядок в доме, голове и мастерской. Хороший день для трав, настоек, растений и спокойной работы. От злости, жадности и лишней суеты лучше отказаться.

Вода дня
Настойки на воде — к исцелению. Пейте чистую воду, заговорённую добрым словом, и день пойдёт ровнее.

Цвет дня
Чёрный, тёмно-синий, коричневый — цвета земли и глубины. В них сегодня сила и опора.

Еда дня
Раздельное питание. Простой хлеб — во благо. Не мешайте лишнего, ешьте спокойно и с благодарностью.

Совет дня
Не начинай новое. Доделай старое. Завершённое дело сегодня дороже трёх начатых.`;

const imageUrl = '/assets/img/cover-default.webp'; // плейсхолдер; бот заменит на CDN-фото

const info = db
  .prepare('INSERT INTO forecast (date, title, summary, body, image_url) VALUES (?, ?, ?, ?, ?)')
  .run(today, title, summary, body, imageUrl);

console.log(`Демо-прогноз добавлен на ${today} (id ${info.lastInsertRowid}).`);
