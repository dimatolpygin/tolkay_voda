// Демо-статьи блога для приёмки этапа 4.
// В проде статьи публикуются через Telegram-бота (этап 5); этот сид нужен,
// чтобы было что показать в сетке и проверить пагинацию (>9 постов).
// Идемпотентен: посты с уже существующим slug пропускаются.
import { db } from '../src/lib/db.js';

const TITLES = [
  ['o-klane', 'О клане «Толкай Вода»', 'Кто мы и зачем толкаем воду — короткое слово о клане и его пути.'],
  ['slava-morzham', 'Слава моржам', 'Почему морж стал символом клана и что он для нас значит.'],
  ['utrennyaya-voda', 'Утренняя вода', 'Простая практика начала дня: стакан воды и доброе слово.'],
  ['pesni-klana', 'Песни клана', 'Откуда берутся наши песни и о чём они на самом деле.'],
  ['tishina-i-rabota', 'Тишина и работа', 'О спокойном труде без суеты — как доделывать начатое.'],
  ['nastoiki-na-vode', 'Настойки на воде', 'Травы, вода и терпение — заметки о домашних настойках.'],
  ['doroga-k-ozeru', 'Дорога к озеру', 'Маленькое путешествие к воде и обратно к себе.'],
  ['prostota-horosha', 'Простота — она хороша', 'О том, как меньше значит больше, словами деда на лавке.'],
  ['zapoved-dnya', 'Заповедь дня', 'Одна мысль, с которой стоит прожить сегодняшний день.'],
  ['zolotoi-pesok', 'Золотой песок', 'Время утекает как песок — о том, на что его тратить.'],
  ['carstvo-ryadom', 'Царство не за горой', 'Оно ближе, чем кажется — заметка о простых вещах.'],
];

function bodyFor(title, excerpt) {
  return `${excerpt}

Клан «Толкай Вода» — это про спокойствие, труд и воду. Мы не спешим и не суетимся: доделываем начатое, наводим порядок в доме, голове и мастерской.

${title} — повод остановиться на минуту. Налить чистой воды, послушать радио клана и вспомнить, что главное делается тихо.

Слава моржам. Толкаем воду вперёд.`;
}

const insert = db.prepare(
  'INSERT INTO posts (slug, title, excerpt, body, image_url, created_at) VALUES (?, ?, ?, ?, ?, ?)'
);
const exists = db.prepare('SELECT id FROM posts WHERE slug = ?');

// Разносим даты по убыванию: первый в списке — самый свежий.
const base = new Date();
let added = 0;

TITLES.forEach(([slug, title, excerpt], i) => {
  if (exists.get(slug)) return;
  const d = new Date(base.getTime() - i * 86400000); // на i дней раньше
  const createdAt = d.toISOString().slice(0, 19).replace('T', ' ');
  insert.run(slug, title, excerpt, bodyFor(title, excerpt), '/assets/img/cover-default.webp', createdAt);
  added++;
});

const total = db.prepare('SELECT COUNT(*) AS n FROM posts').get().n;
console.log(`Добавлено статей: ${added}. Всего в блоге: ${total}.`);
